@click.group(invoke_without_command=True)
@click.pass_context
def trace(ctx: click.Context) -> None:
    """Work with agent execution traces.

    \b
    Examples:
        uipath trace view traces.jsonl
        uipath trace view traces.jsonl --name "get_weather"
        uipath trace view traces.jsonl --contains "get_random*"
    """  # noqa: D301
    if ctx.invoked_subcommand is None:
        click.echo(ctx.get_help())


@trace.command()
@click.argument("file", type=click.Path(exists=True, dir_okay=False))
@click.option(
    "--name",
    default=None,
    help="Filter spans by name (glob pattern, e.g. 'agent*')",
)
@click.option(
    "--eval-id",
    default=None,
    help="Show trace for a specific evaluation by name (eval JSON only)",
)
@click.option(
    "--span-type",
    default=None,
    help="Filter by span_type attribute (e.g. 'TOOL', 'function_call_async')",
)
@click.option(
    "--status",
    default=None,
    type=click.Choice(["ok", "error", "unset"], case_sensitive=False),
    help="Filter by span status",
)
@click.option(
    "--contains",
    "contains_pattern",
    default=None,
    help="Show full subtrees containing a span matching this name (glob pattern)",
)
@click.option(
    "--no-input",
    is_flag=True,
    default=False,
    help="Hide input values",
)
@click.option(
    "--no-output",
    is_flag=True,
    default=False,
    help="Hide output values",
)
@click.option(
    "--full",
    is_flag=True,
    default=False,
    help="Show all span attributes (verbose mode)",
)
def view(
    file: str,
    name: str | None,
    eval_id: str | None,
    span_type: str | None,
    status: str | None,
    contains_pattern: str | None,
    no_input: bool,
    no_output: bool,
    full: bool,
) -> None:
    """Visualize an agent execution trace.

    \b
    Reads trace files produced by:
      - uipath run --trace-file <file.jsonl>
      - uipath eval --trace-file <file.jsonl>
      - uipath eval --verbose --output-file <file.json>

    \b
    Examples:
        uipath trace view traces.jsonl
        uipath trace view traces.jsonl --name "get_weather"
        uipath trace view traces.jsonl --span-type TOOL
        uipath trace view traces.jsonl --status error
        uipath trace view traces.jsonl --contains "get_random*"
        uipath trace view eval_output.json --eval-id "test-case-1"
        uipath trace view traces.jsonl --full
    """  # noqa: D301
    fmt = _detect_format(file)

    if fmt == "eval_json":
        spans = _load_eval_output_spans(file, eval_id)
        if not spans:
            if eval_id:
                console.error(
                    f"No traces found for evaluation '{eval_id}'. "
                    "Make sure the eval was run with --verbose."
                )
            else:
                console.error(
                    "No traces found in the eval output. "
                    "Make sure the eval was run with --verbose."
                )
        is_eval = True
    else:
        spans = _load_jsonl_spans(file)
        if not spans:
            console.error("No spans found in the trace file.")
        is_eval = False

    roots = build_tree(spans, is_eval=is_eval)

    # --contains: keep full subtrees that contain a matching span
    if contains_pattern:
        roots = filter_contains(roots, contains_pattern)

    # Apply filters
    roots = filter_tree(
        roots,
        name_pattern=name,
        span_type_filter=span_type,
        status_filter=status,
    )

    if not roots:
        from rich.console import Console as RichConsole

        RichConsole(force_terminal=True).print(
            "[yellow]No spans match the given filters.[/yellow]"
        )
        return

    render_tree(
        roots,
        show_input=not no_input,
        show_output=not no_output,
        show_full=full,
        max_value_length=0 if full else 200,
    )



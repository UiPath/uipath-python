"""CLI commands for working with agent trajectory traces.

Reads JSONL trace files (from ``uipath run --trace-file``) or eval verbose
JSON output (from ``uipath eval --verbose --output-file``) and renders
a span tree using Rich.
"""

import json
import os
from datetime import datetime, timezone
from typing import Any

import click

from ._utils._console import ConsoleLogger
from ._utils._constants import TRACES_DIR
from ._utils._span_tree import (
    build_tree,
    filter_contains,
    filter_tree,
    render_tree,
)

console = ConsoleLogger()

# Sentinel returned by ``--trace-file`` when the flag is passed without a
# value.  The actual path is then generated by ``default_trace_path()``.
_TRACE_FILE_FLAG = "__auto__"


def default_trace_path(command: str) -> str:
    """Return a timestamped path under ``.uipath/traces/``.

    Args:
        command: The CLI command name (``"run"`` or ``"eval"``), used as a
            filename prefix so ``trace list`` output is easy to scan.

    Returns:
        A path like ``.uipath/traces/run_2026-04-07T14-58-30.jsonl``.
    """
    ts = datetime.now().strftime("%Y-%m-%dT%H-%M-%S")
    return os.path.join(TRACES_DIR, f"{command}_{ts}.jsonl")


def resolve_trace_file(trace_file: str | None, command: str) -> str | None:
    """Resolve *trace_file* from the CLI option to a concrete path.

    * ``None``        → tracing disabled (option not passed)
    * ``_TRACE_FILE_FLAG`` → generate a default path via :func:`default_trace_path`
    * anything else   → use as-is (explicit path from the user)
    """
    if trace_file is None:
        return None
    if trace_file == _TRACE_FILE_FLAG:
        return default_trace_path(command)
    return trace_file


# ---------------------------------------------------------------------------
# Span loading helpers
# ---------------------------------------------------------------------------


def _load_jsonl_spans(path: str) -> list[dict[str, Any]]:
    """Load spans from a JSONL trace file (one OTel span JSON per line)."""
    spans: list[dict[str, Any]] = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                spans.append(json.loads(line))
    return spans


def _load_eval_output_spans(path: str, eval_id: str | None) -> list[dict[str, Any]]:
    """Load spans from an eval verbose JSON output file.

    Returns JSONL-style span dicts (normalised from the
    ``TrajectoryEvaluationSpan`` format stored under
    ``agentExecutionOutput.trace.spans``).
    """
    with open(path, encoding="utf-8") as f:
        data = json.load(f)

    results: list[dict[str, Any]] = data.get("evaluationSetResults", [])

    all_spans: list[dict[str, Any]] = []
    for result in results:
        name = result.get("evaluationName", "")
        if eval_id is not None and name != eval_id:
            continue

        exec_output = result.get("agentExecutionOutput")
        if exec_output is None:
            continue

        trace_data = exec_output.get("trace", {})
        raw_spans = trace_data.get("spans", [])

        for sp in raw_spans:
            all_spans.append(_normalise_eval_span(sp, eval_name=name))

    return all_spans


def _normalise_eval_span(sp: dict[str, Any], eval_name: str) -> dict[str, Any]:
    """Convert a ``TrajectoryEvaluationSpan`` dict into the JSONL-like shape.

    The eval format stores ``parent_name`` instead of ``parent_id``, so we
    keep both representations so the tree builder can work with either.
    """
    return {
        "name": sp.get("name", ""),
        "status": {"status_code": (sp.get("status", "unset") or "unset").upper()},
        "attributes": sp.get("attributes", {}),
        "events": sp.get("events", []),
        # Eval-specific: no span IDs, only parent_name
        "parent_name": sp.get("parent_name"),
        "_eval_name": eval_name,
    }


# ---------------------------------------------------------------------------
# File-format detection
# ---------------------------------------------------------------------------


def _detect_format(path: str) -> str:
    """Return ``'jsonl'`` or ``'eval_json'`` based on file content."""
    if path.endswith(".jsonl"):
        return "jsonl"

    with open(path, encoding="utf-8") as f:
        first_char = f.read(1)

    if first_char == "[" or first_char == "{":
        # Peek inside to see if it looks like eval output
        with open(path, encoding="utf-8") as f:
            try:
                data = json.load(f)
            except json.JSONDecodeError:
                return "jsonl"

        if isinstance(data, dict) and "evaluationSetResults" in data:
            return "eval_json"

    return "jsonl"



_OTEL_SPAN_KEYS = {"name", "context", "start_time", "end_time"}


def _is_otel_jsonl(path: str) -> bool:
    """Check whether *path* looks like an OTel JSONL trace file.

    Peeks at the first non-empty line and checks for characteristic
    OpenTelemetry span keys.
    """
    try:
        with open(path, encoding="utf-8") as f:
            for raw in f:
                line = raw.strip()
                if not line:
                    continue
                obj = json.loads(line)
                if isinstance(obj, dict) and _OTEL_SPAN_KEYS.issubset(obj):
                    return True
                return False
    except (json.JSONDecodeError, OSError, UnicodeDecodeError):
        pass
    return False


def _is_eval_json(path: str) -> bool:
    """Check whether *path* looks like eval verbose JSON output."""
    try:
        with open(path, encoding="utf-8") as f:
            data = json.load(f)
        return isinstance(data, dict) and "evaluationSetResults" in data
    except (json.JSONDecodeError, OSError, UnicodeDecodeError):
        return False


def _count_spans_in_file(path: str, fmt: str) -> int:
    """Return the number of spans in a trace file."""
    try:
        if fmt == "jsonl":
            count = 0
            with open(path, encoding="utf-8") as f:
                for line in f:
                    if line.strip():
                        count += 1
            return count
        else:
            with open(path, encoding="utf-8") as f:
                data = json.load(f)
            total = 0
            for result in data.get("evaluationSetResults", []):
                exec_output = result.get("agentExecutionOutput")
                if exec_output is None:
                    continue
                spans = exec_output.get("trace", {}).get("spans", [])
                total += len(spans)
            return total
    except Exception:
        return 0


def _file_modified_time(path: str) -> datetime:
    """Return the last-modified time of *path* as a tz-aware datetime."""
    ts = os.path.getmtime(path)
    return datetime.fromtimestamp(ts, tz=timezone.utc)


def _discover_trace_files(root: str) -> list[dict[str, Any]]:
    """Walk *root* recursively and return metadata for every trace file found."""
    found: list[dict[str, Any]] = []

    for dirpath, _dirnames, filenames in os.walk(root):
        for fname in filenames:
            full = os.path.join(dirpath, fname)
            rel = os.path.relpath(full, root)

            if fname.endswith(".jsonl") and _is_otel_jsonl(full):
                found.append(
                    {
                        "path": rel,
                        "format": "jsonl",
                        "spans": _count_spans_in_file(full, "jsonl"),
                        "modified": _file_modified_time(full),
                        "size": os.path.getsize(full),
                    }
                )
            elif fname.endswith(".json") and _is_eval_json(full):
                found.append(
                    {
                        "path": rel,
                        "format": "eval_json",
                        "spans": _count_spans_in_file(full, "eval_json"),
                        "modified": _file_modified_time(full),
                        "size": os.path.getsize(full),
                    }
                )

    # Most recently modified first
    found.sort(key=lambda f: f["modified"], reverse=True)
    return found


def _human_size(nbytes: int) -> str:
    """Format *nbytes* as a human-readable string."""
    size: float = nbytes
    for unit in ("B", "KB", "MB", "GB"):
        if size < 1024:
            return f"{size:.0f}{unit}" if unit == "B" else f"{size:.1f}{unit}"
        size /= 1024
    return f"{size:.1f}TB"



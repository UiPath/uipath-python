@trace.command(name="list")
@click.argument(
    "directory",
    default=None,
    required=False,
    type=click.Path(exists=True, file_okay=False),
)
def list_traces(directory: str | None) -> None:
    """Find and list trace files in a directory.

    \b
    Scans DIRECTORY recursively for trace files.  Defaults to
    .uipath/traces (where ``--trace-file`` writes by default).

    \b
    Recognised formats:
      - JSONL trace files from ``uipath run --trace-file``
      - JSONL trace files from ``uipath eval --trace-file``
      - JSON eval output from ``uipath eval --verbose --output-file``

    \b
    Examples:
        uipath trace list
        uipath trace list .uipath/traces
        uipath trace list ./experiments
    """  # noqa: D301
    from rich.console import Console
    from rich.table import Table

    rich_console = Console(force_terminal=True)

    if directory is None:
        directory = TRACES_DIR

    if not os.path.isdir(directory):
        rich_console.print(
            f"[yellow]Trace directory [bold]{directory}[/bold] does not exist. "
            "Run with [bold]--trace-file[/bold] to generate traces.[/yellow]"
        )
        return

    root = os.path.abspath(directory)
    files = _discover_trace_files(root)

    if not files:
        rich_console.print(f"[yellow]No trace files found in {directory}[/yellow]")
        return

    table = Table(title=f"Trace files in [bold]{directory}[/bold]")
    table.add_column("File", style="cyan", no_wrap=True)
    table.add_column("Format", style="dim")
    table.add_column("Spans", justify="right")
    table.add_column("Size", justify="right", style="dim")
    table.add_column("Modified", style="dim")

    now = datetime.now(tz=timezone.utc)
    for f in files:
        age = now - f["modified"]
        if age.total_seconds() < 60:
            when = "just now"
        elif age.total_seconds() < 3600:
            mins = int(age.total_seconds() // 60)
            when = f"{mins}m ago"
        elif age.total_seconds() < 86400:
            hours = int(age.total_seconds() // 3600)
            when = f"{hours}h ago"
        else:
            when = f["modified"].strftime("%Y-%m-%d %H:%M")

        fmt_label = "JSONL" if f["format"] == "jsonl" else "eval JSON"
        table.add_row(
            f["path"],
            fmt_label,
            str(f["spans"]),
            _human_size(f["size"]),
            when,
        )

    rich_console.print(table)
    rich_console.print(
        "\n[dim]Tip: run [bold]uipath trace view <file>[/bold] "
        "to visualize a trace[/dim]"
    )
